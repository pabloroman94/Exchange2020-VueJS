<template>
  <div>
    <bounce-loader :loading="isLoading" :color="'#68b391'" :size="100" />
    <pr-assets-table v-if="!isLoading" :assets="assets" />
  </div>
</template>
<script>
import api from '@/api'
import PrAssetsTable from "@/components/PrAssetsTable";

export default {
  name: "home",
  components: {
    PrAssetsTable
  },
  data(){
    return{
      isLoading: false,
      assets:[]
    }
  },
  created(){
    this.isLoading = true
    api.getAssets()
    .then(assets => (this.assets = assets))
    .finally(()=> this.isLoading = false)
  }
}; 
</script>
